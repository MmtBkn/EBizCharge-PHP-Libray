/**
 * This class stands for EBizChargeServicePause originally named Pause
 * @package EBizCharge
 * @subpackage Services
 * @author Muhammet Bakan <mail@muhammetbakan.com>
 * @version 20150429-01
 * @date 2017-05-25
 */
class EBizChargeServicePause extends EBizChargeWsdlClass
{
    /**
     * Method to call the operation originally named pauseBatchUpload
     * Documentation : Pause a Batch Upload that is already running
     * @uses EBizChargeWsdlClass::getSoapClient()
     * @uses EBizChargeWsdlClass::setResult()
     * @uses EBizChargeWsdlClass::saveLastError()
     * @param EBizChargeStructUeSecurityToken $_token
     * @param integer $_uploadRefNum
     * @return boolean
     */
    public function pauseBatchUpload(EBizChargeStructUeSecurityToken $_token,$_uploadRefNum)
    {
        try
        {
            return $this->setResult(self::getSoapClient()->pauseBatchUpload($_token,$_uploadRefNum));
        }
        catch(SoapFault $soapFault)
        {
            return !$this->saveLastError(__METHOD__,$soapFault);
        }
    }
    /**
     * Returns the result
     * @see EBizChargeWsdlClass::getResult()
     * @return boolean
     */
    public function getResult()
    {
        return parent::getResult();
    }
    /**
     * Method returning the class name
     * @return string __CLASS__
     */
    public function __toString()
    {
        return __CLASS__;
    }
}
