/**
 * This class stands for EBizChargeServiceOverride originally named Override
 * @package EBizCharge
 * @subpackage Services
 * @author Muhammet Bakan <mail@muhammetbakan.com>
 * @version 20150429-01
 * @date 2017-05-25
 */
class EBizChargeServiceOverride extends EBizChargeWsdlClass
{
    /**
     * Method to call the operation originally named overrideTransaction
     * Documentation : Override the transaction specified by RefNum
     * @uses EBizChargeWsdlClass::getSoapClient()
     * @uses EBizChargeWsdlClass::setResult()
     * @uses EBizChargeWsdlClass::saveLastError()
     * @param EBizChargeStructUeSecurityToken $_token
     * @param integer $_refNum
     * @param string $_reason
     * @return boolean
     */
    public function overrideTransaction(EBizChargeStructUeSecurityToken $_token,$_refNum,$_reason)
    {
        try
        {
            return $this->setResult(self::getSoapClient()->overrideTransaction($_token,$_refNum,$_reason));
        }
        catch(SoapFault $soapFault)
        {
            return !$this->saveLastError(__METHOD__,$soapFault);
        }
    }
    /**
     * Returns the result
     * @see EBizChargeWsdlClass::getResult()
     * @return boolean
     */
    public function getResult()
    {
        return parent::getResult();
    }
    /**
     * Method returning the class name
     * @return string __CLASS__
     */
    public function __toString()
    {
        return __CLASS__;
    }
}
